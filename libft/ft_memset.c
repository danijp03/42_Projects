void	*ft_memset(char *s, int c, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s[i] != '\0')
	{
		s[i] = c;
		i++;
	}
	while (s[i] != '\0')
		i++;
	s[i] = '\0';
	return (s);
}
